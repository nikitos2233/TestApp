# –ü–æ–ª—è –¥–ª—è –≤–≤–æ–¥–∞ —á–∏—Å–µ–ª
num1 = st.number_input("–í–≤–µ–¥–∏—Ç–µ –ø–µ—Ä–≤–æ–µ —á–∏—Å–ª–æ", value=0.0)
num2 = st.number_input("–í–≤–µ–¥–∏—Ç–µ –≤—Ç–æ—Ä–æ–µ —á–∏—Å–ª–æ", value=0.0)

# –í—ã–±–æ—Ä –æ–ø–µ—Ä–∞—Ü–∏–∏
operation = st.selectbox(
    "–í—ã–±–µ—Ä–∏—Ç–µ –æ–ø–µ—Ä–∞—Ü–∏—é",
    ("–°–ª–æ–∂–µ–Ω–∏–µ", "–í—ã—á–∏—Ç–∞–Ω–∏–µ", "–£–º–Ω–æ–∂–µ–Ω–∏–µ", "–î–µ–ª–µ–Ω–∏–µ")
)

# –ö–Ω–æ–ø–∫–∞ –¥–ª—è –≤—ã–ø–æ–ª–Ω–µ–Ω–∏—è –æ–ø–µ—Ä–∞—Ü–∏–∏
if st.button("–í—ã—á–∏—Å–ª–∏—Ç—å"):
    if operation == "–°–ª–æ–∂–µ–Ω–∏–µ":
        result = num1 + num2
        st.success(f"–†–µ–∑—É–ª—å—Ç–∞—Ç: {result}")
    elif operation == "–í—ã—á–∏—Ç–∞–Ω–∏–µ":
        result = num1 - num2
        st.success(f"–†–µ–∑—É–ª—å—Ç–∞—Ç: {result}")
    elif operation == "–£–º–Ω–æ–∂–µ–Ω–∏–µ":
        result = num1 * num2
        st.success(f"–†–µ–∑—É–ª—å—Ç–∞—Ç: {result}")
    elif operation == "–î–µ–ª–µ–Ω–∏–µ":
        if num2 != 0:
            result = num1 / num2
            st.success(f"–†–µ–∑—É–ª—å—Ç–∞—Ç: {result}")
        else:
            st.error("–û—à–∏–±–∫–∞: –î–µ–ª–µ–Ω–∏–µ –Ω–∞ –Ω–æ–ª—å!")

